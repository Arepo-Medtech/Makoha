#!/usr/bin/env node
/**
 * schema-authoring-cheatsheet — derive the EXACT authoring constraints for the 7 case-node schemas
 * (Case Corpus v2). `node scripts/schema-authoring-cheatsheet.mjs` → docs/case-authoring/node-authoring-cheatsheet.md
 *
 * WHY: the first v2 case (AUC-017) needed ~20 dry-run iterations to become conformant, because the
 * JSON Schemas are far more prescriptive than the protocol PROSE — exact enums, const values,
 * object-vs-string shapes, and required sub-fields that a transformer cannot guess. Handing a
 * transformer (a Claude Chat session, a workflow agent, or a human) this DERIVED cheat-sheet collapses
 * those iterations to near zero. It is generated FROM the schemas, so it can never drift from them —
 * re-run after any schema change (the protocol's "the schema wins" rule, made operational).
 *
 * Read-only over the schemas. Deterministic.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const NODES = [
  "00_case_envelope", "01_presentation_layer", "02_conversational_policy",
  "10_ground_truth_node", "11_symptom_links_node", "12_management_plan_node", "13_safety_netting_node",
];

/** Walk a schema, emitting one line per constrained leaf: required flag, type, enum, const, item shape. */
function describe(schema, path, out, requiredSet = new Set()) {
  if (!schema || typeof schema !== "object") return;
  const req = new Set(schema.required || []);
  if (schema.type === "object" && schema.properties) {
    for (const [k, v] of Object.entries(schema.properties)) {
      const p = path ? `${path}.${k}` : k;
      const isReq = req.has(k);
      const bits = [];
      if (v.const !== undefined) bits.push(`const=${JSON.stringify(v.const)}`);
      if (v.enum) bits.push(`enum=[${v.enum.map((e) => JSON.stringify(e)).join(", ")}]`);
      if (v.type && !v.enum && v.const === undefined) bits.push(v.type === "array" ? `array<${v.items?.type || "object"}>` : v.type);
      if (bits.length || isReq) out.push(`  ${isReq ? "REQ " : "opt "}${p}${bits.length ? " : " + bits.join(" ") : ""}`);
      // recurse into objects and array-of-object items
      if (v.type === "object" && v.properties) describe(v, p, out);
      if (v.type === "array" && v.items?.type === "object" && v.items.properties) {
        out.push(`  ${isReq ? "REQ " : "opt "}${p}[] (each item — required: ${JSON.stringify(v.items.required || [])}):`);
        describe(v.items, `${p}[]`, out);
      }
    }
  }
}

const lines = [
  "# Case-node authoring cheat-sheet (DERIVED — do not hand-edit)",
  "",
  "> Generated by `scripts/schema-authoring-cheatsheet.mjs` from `data/schemas/*.schema.json`. The",
  "> schema is authoritative; this is its exact shape made legible for authoring. Re-run after any",
  "> schema change. Every `REQ` line MUST be present; every `enum`/`const` value MUST match exactly.",
  "> `additionalProperties:false` is set on the nodes — a key not listed here is REJECTED.",
  "",
  "Authoring lessons from the AUC-017 pilot (the traps that cost iterations):",
  "- Many fields are OBJECTS or ARRAYS-OF-OBJECTS, not strings (e.g. `past_medical_history[]`,",
  "  `patient_education_points[]`, `differential[]`, `diagnostic_reasoning_pitfalls[]`).",
  "- `00.digital_tablet_anchoring.digital_tablet_version` is a **const** (see below) — NOT the omnibus",
  "  version you used. `snomed_edition` is a const string, exact wording matters.",
  "- Enums are strict: `certainty_achievable_via_telehealth`, `verificationStatus_fhir`, `position`,",
  "  `edge_type`, `modifier_type`, `discipline`, `domain`, `care_plan_type`, `necessity`, `target_tier`.",
  "- `scoring_rubric.minimum_domains_required` is an ARRAY of domain names, not a count.",
  "",
];

for (const n of NODES) {
  const schema = JSON.parse(readFileSync(join(ROOT, "data/schemas", `${n}.schema.json`), "utf8"));
  lines.push(`\n## ${n}  —  top-level required: ${JSON.stringify(schema.required || [])}`, "");
  const out = [];
  describe(schema, "", out);
  lines.push("```", ...out, "```");
}

const OUT = join(ROOT, "docs/case-authoring/node-authoring-cheatsheet.md");
writeFileSync(OUT, lines.join("\n") + "\n");
console.log(`node-authoring cheat-sheet → ${OUT.replace(ROOT + "/", "")} (${NODES.length} nodes)`);
